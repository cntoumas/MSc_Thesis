#!/usr/bin/env python3
"""Generate cosim_inputs.h from the RTL golden spectra (results/*/spectrum.npz).

Each test's time-domain input is recovered as round(real(ifft(ref_k))), which
reproduces the *exact* int16 stimulus the RTL testbench used (ref_k is the
golden DFT of that stimulus).  The same 5 vectors drive the HLS cross-check.
"""
import numpy as np, os

REPO = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
npz = np.load(os.path.join(REPO, "results", "serial", "spectrum.npz"),
              allow_pickle=True)
names = [str(s) for s in npz["names"]]
N = 1024
NT = len(names)

inputs = np.zeros((NT, N), dtype=np.int64)
for k in range(NT):
    x = np.fft.ifft(npz[f"ref_{k}"]).real
    xi = np.round(x).astype(np.int64)
    assert np.max(np.abs(x - xi)) < 1e-6, f"test {k} not integer"
    assert xi.min() >= -32768 and xi.max() <= 32767, f"test {k} out of int16"
    inputs[k] = xi

out = os.path.join(os.path.dirname(os.path.abspath(__file__)), "cosim_inputs.h")
with open(out, "w") as f:
    f.write("// Auto-generated by gen_xcheck_inputs.py - do not edit.\n")
    f.write("// 5 test input vectors (int16, real), recovered from the RTL\n")
    f.write("// golden spectra so HLS sees the identical stimulus as the RTL.\n")
    f.write("#pragma once\n#include <stdint.h>\n")
    f.write(f"static const int XC_NTESTS = {NT};\n")
    f.write(f"static const int XC_N = {N};\n")
    f.write("static const char* XC_NAMES[] = {")
    f.write(", ".join(f"\"{n}\"" for n in names))
    f.write("};\n")
    f.write(f"static const int16_t XC_IN[{NT}][{N}] = {{\n")
    for k in range(NT):
        f.write("  {")
        f.write(",".join(str(int(v)) for v in inputs[k]))
        f.write("},\n")
    f.write("};\n")
print("wrote", out, "tests:", names)
