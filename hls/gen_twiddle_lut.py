#!/usr/bin/env python3
"""
Generate twiddle_lut.h — 129-entry (N/8+1) 1/8-cycle compressed cos/sin
ROM for N=1024, Q1.15 fixed-point.  Matches the Verilog twiddle_rom.v
convention exactly: cos/sin over [0, pi/4], same 1/8 symmetry.
"""
import numpy as np
from pathlib import Path

N = 1024
ROM_DEPTH = N // 8 + 1   # 129 entries  (indices 0..128)
MAX_VAL   = (1 << 15) - 1  # 32767 (avoids cos(0) overflow)

cos_lut = [int(np.round(np.cos(2.0 * np.pi * k / N) * MAX_VAL)) for k in range(ROM_DEPTH)]
sin_lut = [int(np.round(np.sin(2.0 * np.pi * k / N) * MAX_VAL)) for k in range(ROM_DEPTH)]

out = Path(__file__).parent / "twiddle_lut.h"
with open(out, "w") as f:
    f.write("#pragma once\n")
    f.write(f"// 1/8-cycle compressed twiddle ROM: N={N}, Q1.15 (cos/sin, 0..pi/4)\n")
    f.write("// Generated by gen_twiddle_lut.py — DO NOT EDIT MANUALLY\n\n")
    f.write(f"static const int TW_ROM_DEPTH = {ROM_DEPTH};\n\n")

    def write_array(f, name, vals):
        f.write(f"static const short {name}[{ROM_DEPTH}] = {{\n")
        for i, v in enumerate(vals):
            sep = "," if i < len(vals) - 1 else " "
            f.write(f"    {v:6d}{sep}")
            if (i + 1) % 8 == 0 or i == len(vals) - 1:
                f.write("\n")
        f.write("};\n\n")

    write_array(f, "TW_COS_LUT", cos_lut)
    write_array(f, "TW_SIN_LUT", sin_lut)

print(f"Generated {out}  ({ROM_DEPTH} entries, N={N})")
