#include "fft_mdf.h"
#include "../twiddle_rom.h"

// ---------------------------------------------------------------------------
// Types used throughout
// ---------------------------------------------------------------------------
typedef ap_int<16> d16_t;   // Q1.15 data word
typedef ap_int<17> d17_t;   // Q2.15 extended (1 bit growth after add/sub)
typedef ap_int<33> d33_t;   // full complex-multiply product

// ---------------------------------------------------------------------------
// Internal inter-stage word.
//
// hls::axis/ap_axis may only appear on top-level AXI-Stream ports (HLS
// 214-208), so the DATAFLOW pipeline carries this plain payload struct
// instead: the 128-bit packed data plus the TLAST flag.  keep/strb are AXI
// sideband signals that are only meaningful at the boundary and are
// regenerated by word_to_axis().
// ---------------------------------------------------------------------------
struct mdf_word_t {
    ap_uint<MDF_P * 32> data;
    ap_uint<1>          last;
};

// ---------------------------------------------------------------------------
// pack / unpack helpers
// ---------------------------------------------------------------------------
static void unpack(const mdf_word_t& pkt, d16_t re[MDF_P], d16_t im[MDF_P]) {
#pragma HLS INLINE
    for (int p = 0; p < MDF_P; p++) {
#pragma HLS UNROLL
        re[p] = pkt.data.range(p * 32 + 15, p * 32);
        im[p] = pkt.data.range(p * 32 + 31, p * 32 + 16);
    }
}

static void pack(mdf_word_t& pkt, const d16_t re[MDF_P], const d16_t im[MDF_P],
                 bool last = false) {
#pragma HLS INLINE
    for (int p = 0; p < MDF_P; p++) {
#pragma HLS UNROLL
        pkt.data.range(p * 32 + 15, p * 32)    = re[p];
        pkt.data.range(p * 32 + 31, p * 32 + 16) = im[p];
    }
    pkt.last = last ? 1 : 0;
}

// ---------------------------------------------------------------------------
// round-to-even right shift by 1 (matches RTL block_scaler)
// ---------------------------------------------------------------------------
static d16_t rte_shr1(d17_t v) {
#pragma HLS INLINE
    return (d16_t)((v + (d17_t)(v[0] & v[1])) >> 1);
}

// ---------------------------------------------------------------------------
// Complex multiply: (a_re + j*a_im) × (w_re + j*w_im), Q1.15 × Q1.15 → Q1.15
// Matches RTL complex_mult (truncate to 16-bit after >> 15).
// ---------------------------------------------------------------------------
static void cmult(d16_t a_re, d16_t a_im, short w_re, short w_im,
                  d16_t& p_re, d16_t& p_im) {
#pragma HLS INLINE
    d33_t pr = (d33_t)a_re * w_re - (d33_t)a_im * w_im;
    d33_t pi = (d33_t)a_re * w_im + (d33_t)a_im * w_re;
    p_re = (d16_t)(pr >> 15);
    p_im = (d16_t)(pi >> 15);
}

// ---------------------------------------------------------------------------
// Feedback stage (stages 0–7).
//
// Template parameters:
//   STAGE — feedback stage index (0..7)
//   DEPTH — delay-line depth = N / (P * 2^(STAGE+1))
//
// For each group of P samples:
//   • cyc % (2*DEPTH) < DEPTH  → load phase:
//       store DIN in delay line; multiply dl_out by twiddle → output
//   • cyc % (2*DEPTH) >= DEPTH → butterfly phase:
//       butterfly DIN vs dl_out; store scaled diff; output scaled sum
//
// Static state (delay_re / delay_im) persists across calls, modelling
// the RTL's circular LUTRAM delay line.
// ---------------------------------------------------------------------------
template<int STAGE, int DEPTH>
static void stage_fb(hls::stream<mdf_word_t>& din,
                     hls::stream<mdf_word_t>& dout) {
#pragma HLS INLINE

    static d16_t delay_re[DEPTH][MDF_P];
    static d16_t delay_im[DEPTH][MDF_P];
#pragma HLS ARRAY_PARTITION variable=delay_re dim=2 complete
#pragma HLS ARRAY_PARTITION variable=delay_im dim=2 complete

    for (int cyc = 0; cyc < MDF_WORDS; cyc++) {
#pragma HLS PIPELINE II=1
#pragma HLS DEPENDENCE variable=delay_re inter false
#pragma HLS DEPENDENCE variable=delay_im inter false

        mdf_word_t x = din.read();
        d16_t in_re[MDF_P], in_im[MDF_P];
        unpack(x, in_re, in_im);

        int ptr          = cyc % DEPTH;
        bool is_bfly     = (cyc % (2 * DEPTH)) >= DEPTH;

        // Read delay line (async read — combinational, same clock edge)
        d16_t dl_re[MDF_P], dl_im[MDF_P];
        for (int p = 0; p < MDF_P; p++) {
#pragma HLS UNROLL
            dl_re[p] = delay_re[ptr][p];
            dl_im[p] = delay_im[ptr][p];
        }

        d16_t out_re[MDF_P], out_im[MDF_P];

        if (!is_bfly) {
            // Load phase: store DIN; multiply dl_out × twiddle → output
            for (int p = 0; p < MDF_P; p++) {
#pragma HLS UNROLL
                delay_re[ptr][p] = in_re[p];
                delay_im[ptr][p] = in_im[p];

                short w_re, w_im;
                mdf_twiddle_get(ptr, STAGE, p, &w_re, &w_im);
                cmult(dl_re[p], dl_im[p], w_re, w_im, out_re[p], out_im[p]);
            }
        } else {
            // Butterfly phase: butterfly dl_out vs DIN;
            //   diff (BFP-scaled) → delay line
            //   sum  (BFP-scaled) → output
            for (int p = 0; p < MDF_P; p++) {
#pragma HLS UNROLL
                d17_t sum_re  = (d17_t)dl_re[p] + in_re[p];
                d17_t sum_im  = (d17_t)dl_im[p] + in_im[p];
                d17_t diff_re = (d17_t)dl_re[p] - in_re[p];
                d17_t diff_im = (d17_t)dl_im[p] - in_im[p];

                delay_re[ptr][p] = rte_shr1(diff_re);
                delay_im[ptr][p] = rte_shr1(diff_im);
                out_re[p]        = rte_shr1(sum_re);
                out_im[p]        = rte_shr1(sum_im);
            }
        }

        mdf_word_t y;
        pack(y, out_re, out_im, (bool)x.last);
        dout.write(y);
    }
}

// ---------------------------------------------------------------------------
// No-feedback stages (stages 8 and 9).
//
// IDX=0 (stage 8): wide butterfly — pairs (path0,path2) and (path1,path3)
//   Twiddle: W_4^0=1 (no-op) and W_4^1=-j (re_out=+im_in, im_out=-re_in)
//
// IDX=1 (stage 9): narrow butterfly — pairs (path0,path1) and (path2,path3)
//   Twiddle: W_2^0=1 (both no-op)
//
// BFP: fixed right-shift by 1 applied to all outputs.
// ---------------------------------------------------------------------------
template<int IDX>
static void stage_nf(hls::stream<mdf_word_t>& din,
                     hls::stream<mdf_word_t>& dout) {
#pragma HLS INLINE

    for (int cyc = 0; cyc < MDF_WORDS; cyc++) {
#pragma HLS PIPELINE II=1

        mdf_word_t x = din.read();
        d16_t in_re[MDF_P], in_im[MDF_P];
        unpack(x, in_re, in_im);

        d17_t out_re[MDF_P], out_im[MDF_P];

        if (IDX == 0) {
            // Stage 8: pairs (0,2) and (1,3)
            // Pair A: sum/diff of paths 0 and 2, twiddle = W_4^0 = 1
            d17_t sum_re_A  = (d17_t)in_re[0] + in_re[2];
            d17_t sum_im_A  = (d17_t)in_im[0] + in_im[2];
            d17_t diff_re_A = (d17_t)in_re[0] - in_re[2];
            d17_t diff_im_A = (d17_t)in_im[0] - in_im[2];

            // Pair B: sum/diff of paths 1 and 3, twiddle = W_4^1 = -j
            //   diff × (-j): re_out = +diff_im, im_out = -diff_re
            d17_t sum_re_B  = (d17_t)in_re[1] + in_re[3];
            d17_t sum_im_B  = (d17_t)in_im[1] + in_im[3];
            d17_t diff_re_B = (d17_t)in_re[1] - in_re[3];
            d17_t diff_im_B = (d17_t)in_im[1] - in_im[3];

            // Output assignment (matches RTL bf_packed → g_pack_out):
            //   path 0 ← sum_A
            out_re[0] = sum_re_A;  out_im[0] = sum_im_A;
            //   path 1 ← sum_B
            out_re[1] = sum_re_B;  out_im[1] = sum_im_B;
            //   path 2 ← diff_A × W_4^0 = diff_A  (no-op)
            out_re[2] = diff_re_A; out_im[2] = diff_im_A;
            //   path 3 ← diff_B × W_4^1 = diff_B × (-j)
            out_re[3] =  diff_im_B;
            out_im[3] = -diff_re_B;
        } else {
            // Stage 9: pairs (0,1) and (2,3), both twiddles = W_2^0 = 1
            d17_t sum_re_A  = (d17_t)in_re[0] + in_re[1];
            d17_t sum_im_A  = (d17_t)in_im[0] + in_im[1];
            d17_t diff_re_A = (d17_t)in_re[0] - in_re[1];
            d17_t diff_im_A = (d17_t)in_im[0] - in_im[1];

            d17_t sum_re_B  = (d17_t)in_re[2] + in_re[3];
            d17_t sum_im_B  = (d17_t)in_im[2] + in_im[3];
            d17_t diff_re_B = (d17_t)in_re[2] - in_re[3];
            d17_t diff_im_B = (d17_t)in_im[2] - in_im[3];

            // Output: [sum_A, diff_A, sum_B, diff_B]
            out_re[0] = sum_re_A;  out_im[0] = sum_im_A;
            out_re[1] = diff_re_A; out_im[1] = diff_im_A;
            out_re[2] = sum_re_B;  out_im[2] = sum_im_B;
            out_re[3] = diff_re_B; out_im[3] = diff_im_B;
        }

        // Fixed BFP: right-shift all outputs by 1
        d16_t scaled_re[MDF_P], scaled_im[MDF_P];
        for (int p = 0; p < MDF_P; p++) {
#pragma HLS UNROLL
            scaled_re[p] = rte_shr1(out_re[p]);
            scaled_im[p] = rte_shr1(out_im[p]);
        }

        mdf_word_t y;
        pack(y, scaled_re, scaled_im, (bool)x.last);
        dout.write(y);
    }
}

// ---------------------------------------------------------------------------
// Bit-reversal reorder buffer.
//
// Collects MDF_WORDS groups into a ping-pong buffer, then reads back in
// bit-reversed address order (8-bit reversal of the 8-bit group index),
// swapping paths 1↔2.  Adds MDF_WORDS = 256 cycles of latency.
//
// Static state persists across calls.
// ---------------------------------------------------------------------------
static void bit_reverse_buf(hls::stream<mdf_word_t>& din,
                             hls::stream<mdf_word_t>& dout) {
#pragma HLS INLINE

    static const int WORDS  = MDF_WORDS;         // 256
    static const int BUS_W  = MDF_P * 32;        // 128 bits packed

    static ap_uint<128> bank[2][256];
#pragma HLS ARRAY_PARTITION variable=bank dim=1 complete

    static int  wr_bank = 0;
    static int  wr_cnt  = 0;

    // Write phase: receive one block
    write_loop: for (int i = 0; i < WORDS; i++) {
#pragma HLS PIPELINE II=1
        mdf_word_t x = din.read();
        bank[wr_bank][wr_cnt] = x.data;
        wr_cnt++;
        if (wr_cnt == WORDS) wr_cnt = 0;
    }

    // Swap write bank
    int rd_bank = wr_bank;
    wr_bank = 1 - wr_bank;

    // Read phase: bit-reversed address, path 1↔2 swap
    read_loop: for (int i = 0; i < WORDS; i++) {
#pragma HLS PIPELINE II=1

        // 8-bit bit reversal
        ap_uint<8> rev = 0;
        ap_uint<8> idx = (ap_uint<8>)i;
        for (int b = 0; b < 8; b++) {
#pragma HLS UNROLL
            rev[7 - b] = idx[b];
        }

        ap_uint<128> raw = bank[rd_bank][(int)rev];

        // Swap paths 1 and 2 (32-bit slots)
        mdf_word_t y;
        y.data.range( 31,  0) = raw.range( 31,  0);  // path 0 unchanged
        y.data.range( 63, 32) = raw.range( 95, 64);  // path 1 ← raw path 2
        y.data.range( 95, 64) = raw.range( 63, 32);  // path 2 ← raw path 1
        y.data.range(127, 96) = raw.range(127, 96);  // path 3 unchanged
        y.last = (i == WORDS - 1) ? 1 : 0;
        dout.write(y);
    }
}

// ---------------------------------------------------------------------------
// Boundary adapters: AXI-Stream port <-> internal plain word.
// These isolate the ap_axis type to the top-level ports (HLS 214-208).
// ---------------------------------------------------------------------------
static void axis_to_word(hls::stream<mdf_pkt_t>&  in,
                         hls::stream<mdf_word_t>& out) {
    for (int i = 0; i < MDF_WORDS; i++) {
#pragma HLS PIPELINE II=1
        mdf_pkt_t  x = in.read();
        mdf_word_t w;
        w.data = x.data;
        w.last = x.last;
        out.write(w);
    }
}

static void word_to_axis(hls::stream<mdf_word_t>& in,
                         hls::stream<mdf_pkt_t>&  out) {
    for (int i = 0; i < MDF_WORDS; i++) {
#pragma HLS PIPELINE II=1
        mdf_word_t w = in.read();
        mdf_pkt_t  x;
        x.data = w.data;
        x.last = w.last;
        x.keep = -1;
        x.strb = -1;
        out.write(x);
    }
}

// ---------------------------------------------------------------------------
// Top-level function
// ---------------------------------------------------------------------------
void fft_mdf(
    hls::stream<mdf_pkt_t>& s_in,
    hls::stream<mdf_pkt_t>& s_out
) {
#pragma HLS INTERFACE axis port=s_in  register
#pragma HLS INTERFACE axis port=s_out register
#pragma HLS INTERFACE ap_ctrl_none port=return

#pragma HLS DATAFLOW

    // Inter-stage streams (plain words).  depth=MDF_WORDS+4 gives HLS enough
    // slack to pipeline across stage boundaries in DATAFLOW mode.
    hls::stream<mdf_word_t> w_in, w_out;
    hls::stream<mdf_word_t> s0, s1, s2, s3, s4, s5, s6, s7, s8, s9;
#pragma HLS STREAM variable=w_in  depth=260
#pragma HLS STREAM variable=w_out depth=260
#pragma HLS STREAM variable=s0 depth=260
#pragma HLS STREAM variable=s1 depth=260
#pragma HLS STREAM variable=s2 depth=260
#pragma HLS STREAM variable=s3 depth=260
#pragma HLS STREAM variable=s4 depth=260
#pragma HLS STREAM variable=s5 depth=260
#pragma HLS STREAM variable=s6 depth=260
#pragma HLS STREAM variable=s7 depth=260
#pragma HLS STREAM variable=s8 depth=260
#pragma HLS STREAM variable=s9 depth=260

    // AXI-Stream input -> internal word
    axis_to_word(s_in, w_in);

    // 8 feedback stages: DEPTH = N/(P*2^(STAGE+1))
    stage_fb<0, 128>(w_in, s0);
    stage_fb<1,  64>(s0,   s1);
    stage_fb<2,  32>(s1,   s2);
    stage_fb<3,  16>(s2,   s3);
    stage_fb<4,   8>(s3,   s4);
    stage_fb<5,   4>(s4,   s5);
    stage_fb<6,   2>(s5,   s6);
    stage_fb<7,   1>(s6,   s7);

    // 2 no-feedback stages (trivial ±1/±j twiddles, no DSPs)
    stage_nf<0>(s7, s8);
    stage_nf<1>(s8, s9);

    // Bit-reversal reorder
    bit_reverse_buf(s9, w_out);

    // Internal word -> AXI-Stream output
    word_to_axis(w_out, s_out);
}
